	// Export data in HTML/CSV/Word/Excel/Email/PDF format
	function ExportDocument(&$Doc, &$Recordset, $StartRec, $StopRec, $ExportPageType = "") {
		if (!$Recordset || !$Doc)
			return;
		if (!$Doc->ExportCustom) {

			// Write header
			$Doc->ExportTableHeader();
			if ($Doc->Horizontal) { // Horizontal format, write header
				$Doc->BeginExportRow();
				if ($ExportPageType == "view") {
					if ($this->wegs_id->Exportable) $Doc->ExportCaption($this->wegs_id);
					if ($this->fk_wegs->Exportable) $Doc->ExportCaption($this->fk_wegs);
					if ($this->item->Exportable) $Doc->ExportCaption($this->item);
					if ($this->est_quantity->Exportable) $Doc->ExportCaption($this->est_quantity);
					if ($this->est_cost->Exportable) $Doc->ExportCaption($this->est_cost);
				} else {
					if ($this->wegs_id->Exportable) $Doc->ExportCaption($this->wegs_id);
					if ($this->fk_wegs->Exportable) $Doc->ExportCaption($this->fk_wegs);
					if ($this->item->Exportable) $Doc->ExportCaption($this->item);
					if ($this->est_quantity->Exportable) $Doc->ExportCaption($this->est_quantity);
					if ($this->est_cost->Exportable) $Doc->ExportCaption($this->est_cost);
				}
				$Doc->EndExportRow();
			}
		}

		// Move to first record
		$RecCnt = $StartRec - 1;
		if (!$Recordset->EOF) {
			$Recordset->MoveFirst();
			if ($StartRec > 1)
				$Recordset->Move($StartRec - 1);
		}
		while (!$Recordset->EOF && $RecCnt < $StopRec) {
			$RecCnt++;
			if (intval($RecCnt) >= intval($StartRec)) {
				$RowCnt = intval($RecCnt) - intval($StartRec) + 1;

				// Page break
				if ($this->ExportPageBreakCount > 0) {
					if ($RowCnt > 1 && ($RowCnt - 1) % $this->ExportPageBreakCount == 0)
						$Doc->ExportPageBreak();
				}
				$this->LoadListRowValues($Recordset);

				// Render row
				$this->RowType = EW_ROWTYPE_VIEW; // Render view
				$this->ResetAttrs();
				$this->RenderListRow();
				if (!$Doc->ExportCustom) {
					$Doc->BeginExportRow($RowCnt); // Allow CSS styles if enabled
					if ($ExportPageType == "view") {
						if ($this->wegs_id->Exportable) $Doc->ExportField($this->wegs_id);
						if ($this->fk_wegs->Exportable) $Doc->ExportField($this->fk_wegs);
						if ($this->item->Exportable) $Doc->ExportField($this->item);
						if ($this->est_quantity->Exportable) $Doc->ExportField($this->est_quantity);
						if ($this->est_cost->Exportable) $Doc->ExportField($this->est_cost);
					} else {
						if ($this->wegs_id->Exportable) $Doc->ExportField($this->wegs_id);
						if ($this->fk_wegs->Exportable) $Doc->ExportField($this->fk_wegs);
						if ($this->item->Exportable) $Doc->ExportField($this->item);
						if ($this->est_quantity->Exportable) $Doc->ExportField($this->est_quantity);
						if ($this->est_cost->Exportable) $Doc->ExportField($this->est_cost);
					}
					$Doc->EndExportRow();
				}
			}

			// Call Row Export server event
			if ($Doc->ExportCustom)
				$this->Row_Export($Recordset->fields);
			$Recordset->MoveNext();
		}
		if (!$Doc->ExportCustom) {
			$Doc->ExportTableFooter();
		}
	}

	// Get auto fill value
	function GetAutoFill($id, $val) {
		$rsarr = array();
		$rowcnt = 0;

		// Output
		if (is_array($rsarr) && $rowcnt > 0) {
			$fldcnt = count($rsarr[0]);
			for ($i = 0; $i < $rowcnt; $i++) {
				for ($j = 0; $j < $fldcnt; $j++) {
					$str = strval($rsarr[$i][$j]);
					$str = ew_ConvertToUtf8($str);
					if (isset($post["keepCRLF"])) {
						$str = str_replace(array("\r", "\n"), array("\\r", "\\n"), $str);
					} else {
						$str = str_replace(array("\r", "\n"), array(" ", " "), $str);
					}
					$rsarr[$i][$j] = $str;
				}
			}
			return ew_ArrayToJson($rsarr);
		} else {
			return FALSE;
		}
	}

